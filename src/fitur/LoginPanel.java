    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // --- INI ADALAH LOGIKA BARU UNTUK TOMBOL LOGIN ---

        // 1. Dapatkan teks dari field
        String inputUsername = username.getText();
        String inputPassword = new String(txtPassword.getPassword()); // Cara aman ambil password

        // 2. Validasi (pastikan tidak kosong)
        if (inputUsername.isEmpty() || inputPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Hentikan eksekusi
        }

        // 3. Siapkan koneksi dan query
        // --- PERUBAHAN DI SINI: Kita juga ambil ID dan NAMA ---
        String sql = "SELECT users_id, nama, password, jabatan FROM users WHERE username = ?";
        
        // Kita gunakan try-with-resources agar koneksi otomatis tertutup
        try (Connection conn = DbConnection.getConnection(); 
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            // 4. Set parameter query dengan aman
            stmt.setString(1, inputUsername);

            // 5. Eksekusi query
            try (ResultSet rs = stmt.executeQuery()) {
                
                // 6. Cek Hasil
                if (rs.next()) {
                    // --- Username DITEMUKAN ---
                    String passwordDariDb = rs.getString("password");
                    String jabatan = rs.getString("jabatan");

                    // 7. Bandingkan password
                    // PENTING: Nanti ganti ini dengan pengecekan HASH (Bcrypt)
                    // Untuk sekarang, kita bandingkan teks biasa
                    if (inputPassword.equals(passwordDariDb)) {
                        // --- LOGIN BERHASIL ---
                        
                        // --- PERUBAHAN DI SINI: Ambil ID dan NAMA ---
                        int userId = rs.getInt("users_id");
                        String nama = rs.getString("nama");
                        
                        JOptionPane.showMessageDialog(this, "Login Berhasil! Selamat datang, " + nama);

                        // 8. Pindah Halaman berdasarkan Jabatan
                        if (jabatan.equals("Pemilik")) {
                            // --- AKTIFKAN INI JIKA SUDAH ADA ---
                            logger.info("Membuka Dashboard Pemilik...");
                            // Ganti 'DashboardPemilik' dengan nama JFrame Anda
                            // DashboardPemilik pemilikFrame = new DashboardPemilik(userId, nama);
                            // pemilikFrame.setVisible(true);
                            
                        } else {
                            // --- KARYAWAN BIASA (Kasir, Koki, etc.) ---
                            
                            // 1. Log message (Ini yang Anda lihat)
                            logger.info("Membuka Dashboard Karyawan...");

                            // 2. INI YANG MEMBUKA JENDELA BARU
                            // Pastikan nama kelasnya 'DashboardKaryawan'
                            // dan memiliki constructor (int, String)
                            DashboardKaryawan karyawanFrame = new DashboardKaryawan(userId, nama);
                            karyawanFrame.setVisible(true);
                        }
                        
                        // 3. Tutup jendela login ini
                        this.dispose(); 

                    } else {
                        // --- Password Salah ---
                        JOptionPane.showMessageDialog(this, "Password salah!", "Error", JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    // --- Username TIDAK Ditemukan ---
                    JOptionPane.showMessageDialog(this, "Username tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (SQLException e) {
            // Tangani jika ada error SQL (koneksi putus, query salah, dll)
            logger.severe("Error Database: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Terjadi error pada database. Silakan hubungi admin.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginActionPerformed

