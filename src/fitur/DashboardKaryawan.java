    private void exitBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exitBTActionPerformed
        // 1. Tampilkan dialog konfirmasi (Alert)
        int jawaban = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "Apakah Anda yakin ingin keluar (Logout)?", 
            "Konfirmasi Keluar", 
            javax.swing.JOptionPane.YES_NO_OPTION, 
            javax.swing.JOptionPane.QUESTION_MESSAGE
        );
        
        // 2. Cek jawaban user
        if (jawaban == javax.swing.JOptionPane.YES_OPTION) {
            // 3. Buka kembali LoginPanel
            LoginPanel loginFrame = new LoginPanel();
            loginFrame.setVisible(true);
            
            // 4. Tutup dashboard saat ini
            this.dispose();
        }
        // Jika pilih NO, tidak terjadi apa-apa (tetap di dashboard)
    }//GEN-LAST:event_exitBTActionPerformed
