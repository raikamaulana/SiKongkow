    private void forgotPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_forgotPasswordMouseClicked
        // 1. Tampilkan dialog konfirmasi
        int response = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin lupa password dan ingin melanjutkan?", 
                "Konfirmasi Lupa Password", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        
        // 2. Cek jika pengguna mengklik "Yes" (YA)
        if (response == JOptionPane.YES_OPTION) {
            // Pengguna mengklik "Ya"
            
            // 3. Buka frame ForgotPasswordPanel
            // PASTIKAN Anda sudah memiliki file JFrame bernama 'ForgotPasswordPanel.java'
            logger.info("Membuka panel Lupa Password...");
            ForgotPasswordPanel forgotFrame = new ForgotPasswordPanel();
            forgotFrame.setVisible(true);
            
            // 4. Tutup frame login ini
            this.dispose();
            
        } else {
            // Pengguna mengklik "Tidak" atau menutup dialog
            // Tidak perlu melakukan apa-apa
            logger.info("Lupa Password dibatalkan.");
        }
    }//GEN-LAST:event_forgotPasswordMouseClicked

