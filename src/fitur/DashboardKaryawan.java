    private void showAbsensiPanel() {
        // 1. Buat Window Absensi Baru
        // Kita kirim ID dan NAMA agar nanti AbsensiPanel bisa buka Dashboard lagi
        AbsensiPanel absensiWindow = new AbsensiPanel(this.loggedInUserId, this.loggedInNama);
        
        // 2. Tampilkan Window Absensi
        absensiWindow.setVisible(true);
        
        // 3. Tutup Dashboard saat ini
        this.dispose();
    }
    
